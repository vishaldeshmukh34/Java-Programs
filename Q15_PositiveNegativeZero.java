// 15. Write a program to accept a number and find if it is Zero, Positive or Negative.

public class Q15_PositiveNegativeZero {
    public static void main(String[] args) {
        int a = 23 ;

        if(a == 0)
        {
            System.out.println("Zero");
        }
        else if(a > 0)
        {
            System.out.println("Positive");
        }
        else
        {
            System.out.println("Negative");
        }
    }
}
