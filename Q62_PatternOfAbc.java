public class Q62_PatternOfAbc {
    

    public static void main(String[] args) {
          for(char i = 'A' ; i <= 'H' ; i++)
        {
            for(char j = 'A' ; j <= 'H' ; j++)
            {
                System.out.print(i);
            }
            System.out.println();
        }
    }
}
