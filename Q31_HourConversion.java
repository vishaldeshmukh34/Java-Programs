// 31. Write a Java program to convert hours into minutes and seconds.
public class Q31_HourConversion {
    public static void main(String[] args) {
        

        int h = 1 ;

        int m = 60 ;
         
        int c = h * m  * 60;

        System.out.println(h);
        System.out.println(m);
        System.out.println(c);

        System.out.println(c);
    }
}
