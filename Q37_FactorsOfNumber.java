// 37. Write a Java program to print all factors of a given number.
public class Q37_FactorsOfNumber {
    public static void main(String[] args) {
        
            int n = 5 ;
            int f = 1 ;

      for(int i = 1 ;  i <= n ; i++ )
     {

        f = f * i ;
        System.out.println(f);
     }


    }
}
