// 14. Write a program to accept a number and find if it is even or odd.


public class Q14_EvenOrOdd {
    public static void main(String[] args) {
        int a = 2 ;

        if(a % 2 == 0)
        {
            System.out.println("even");
        }
        else
        {
            System.out.println("Odd");
        }
    }
}
