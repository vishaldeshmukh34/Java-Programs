// 33. Write a Java program to print the squares of numbers from 1 to n.
public class Q33_SquaresOfNumbers {
    public static void main(String[] args) {
        
        int n  = 50 ;
      
        
        for(int i = 1 ; i <= n ; i++)
        {

            int squares = i * i ;
            System.out.println(squares);
        }
        

    }
}
