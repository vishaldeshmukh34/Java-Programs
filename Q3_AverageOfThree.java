// 3. WAP to calculate average of three integer nos.


public class Q3_AverageOfThree {
    public static void main(String[] args) {
        

        int a = 60 ;
        int b = 30 ;
        int c = 21 ;

        int sum = a + b + c ;
        double average = sum / 3 ;
        System.out.println(average);
        
    }
}
