// 50. Generate the series 1, 2, 3, 4, 5, 4, 3, 2, 1.
public class Q50_MirrorNumberSeries {
    public static void main(String[] args) {
        
            for(int i = 1 ; i <= 5 ; i++ )
            {
                System.out.println(i);
            }
            for(int i = 4 ; i >= 1 ; i-- )
            {
                System.out.println(i);
            }
    }
}
